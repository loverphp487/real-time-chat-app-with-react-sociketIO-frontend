import { z } from 'zod';

//////////////////////////////////////////////////////
/////////////////////////////////////////////////////
/// handel name error schema
/////////////////////////////////////////////////////
const nameSchema = z
	.string()
	.trim()
	.min(4, { message: 'should contain at least 4 character(s)' })
	.max(50);

//////////////////////////////////////////////////////
/////////////////////////////////////////////////////
/// handel email error schema
/////////////////////////////////////////////////////
const emailSchema = z.string().trim().email('Invalid email address');

//////////////////////////////////////////////////////
/////////////////////////////////////////////////////
/// handel password error schema
/////////////////////////////////////////////////////
const passwordSchema = z
	.string()
	.trim()
	.min(6, { message: 'should contain at least 6 character(s)' })
	.max(50);

//////////////////////////////////////////////////////
/////////////////////////////////////////////////////
/// handel registration error schema
/////////////////////////////////////////////////////

export const registerSchema = z.object({
	firstName: nameSchema,
	email: emailSchema,
	password: passwordSchema,
});

//////////////////////////////////////////////////////
/////////////////////////////////////////////////////
/// handel login error schema
/////////////////////////////////////////////////////
export const loginSchema = z.object({
	email: emailSchema,
	password: passwordSchema,
});
